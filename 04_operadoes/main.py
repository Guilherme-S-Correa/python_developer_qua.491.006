# declaraçao de variaveis 
x = 5
y = 2

# operacoes

soma = x + y
subtracao = x -y 
multiplicacao = x*y
divisao = x/y
resto = x%y
potencia = x**y

# saida de dados 

print("a soma de {} e {} é {}.".format(x, y , soma))  
print(f"A subtração de {x} e {y} é {subtracao}.")
print(f"A duvisão de {x} e {y} é {divisao}.")
print(f"A multiplicação de {x} e {y} é {multiplicacao}.")
print(f"O resto da divisão {x} e {y} é {resto}.")
print(f"{x} elevado a {y} é {potencia}.")